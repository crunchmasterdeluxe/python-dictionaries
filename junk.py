def get_items(d):
    """
    Without looping, add the dictionary's items (key-value pairs) to a variable named `d_items`
    """

    # put your code here
    #####################
    d_items = d.items()
    #####################

    return d_items


def get_keys(d):
    """
    Without looping, add the dictionary's keys (not values) to a variable named `d_keys`
    """

    # put your code here
    #####################
    d_keys = d.keys()[0]
    #####################

    return d_keys


def get_values(d):
    """
    Without looping, add the dictionary's values (not keys) to a variable named `d_values`
    """

    # put your code here
    #####################
    d_values = d.values()
    #####################

    return d_values
